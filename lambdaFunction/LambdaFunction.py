# Lambda function
# Small one line function

mul_func = lambda x, y: x * y


# Function call
print(mul_func(6, 4))


"""
| Part   | Meaning                           |
| ------ | --------------------------------- |
| lambda | Keyword for lambda function       |
| x, y   | Parameters                        |
| :      | Separates input and expression    |
| x * y  | Expression returned automatically |
"""